class GuessingGame
  RESULT_OF_GUESS_MESSAGE = {
    high:  "Your number is too high.",
    low:   "Your number is too low.",
    match: "That's the number!"
  }.freeze

  WIN_OR_LOSE = {
    high:  :lose,
    low:   :lose,
    match: :win
  }.freeze

  RESULT_OF_GAME_MESSAGE = {
    win:  "You won!",
    lose: "You have no more guesses. You lost!"
  }.freeze

  attr_reader :range_min, :range_max

  def initialize range_min, range_max
    @secret_number = nil
    @range_min = range_min
    @range_max = range_max
    set_RANGE_constant
    set_MAX_GUESSES_constant
  end

  def set_RANGE_constant
    self.class.const_set('RANGE', convert_to_range(range_min, range_max))
  end

  def set_MAX_GUESSES_constant
    size_of_range = range_max - range_min
    size_of_range
    self.class.const_set('MAX_GUESSES', Math.log2(size_of_range).to_i + 1 )
  end

  def play
    reset
    game_result = play_game
    display_game_end_message(game_result)
  end

  private
  attr_reader :range

  def convert_to_range range_min, range_max
    Range.new(range_min, range_max)
  end

  def reset
    @secret_number = rand(RANGE)
  end

  def play_game
    result = nil
    MAX_GUESSES.downto(1) do |remaining_guesses|
      display_guesses_remaining(remaining_guesses)
      result = check_guess(obtain_one_guess)
      puts RESULT_OF_GUESS_MESSAGE[result]
      break if result == :match
    end
    WIN_OR_LOSE[result]
  end

  def display_guesses_remaining(remaining)
    puts
    if remaining == 1
      puts 'You have 1 guess remaining.'
    else
      puts "You have #{remaining} guesses remaining."
    end
  end

  def obtain_one_guess
    loop do
      print "Enter a number between #{RANGE.first} and #{RANGE.last}: "
      guess = gets.chomp.to_i
      return guess if RANGE.cover?(guess)
      print "Invalid guess. "
    end
  end

  def check_guess(guess_value)
    return :match if guess_value == @secret_number
    return :low if guess_value < @secret_number
    :high
  end

  def display_game_end_message(result)
    puts "", RESULT_OF_GAME_MESSAGE[result]
  end
end

def app
  game = GuessingGame.new(501, 1500)
  game.play
end

